        System.out.println(i2);
        System.out.println(i8);
        System.out.println(i10);
        System.out.println(i16);

        short s2 = 0b10100010100;
        short s8 = 02424;
        short s10 = 1300;
        short s16= 514;

        System.out.println(s2);
        System.out.println(s8);
        System.out.println(s10);
        System.out.println(s16);

